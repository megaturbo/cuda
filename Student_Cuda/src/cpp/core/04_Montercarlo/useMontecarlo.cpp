bool useMontecarlo()
{
	float a = -1.0f;
	float b = 1.0f;
	float M = 1.0f;
	int nbFlechettes = INT_MAX / 10;

	Montecarlo montecarlo(a, b, M, nbFlechettes);
	montecarlo.run();
	float pi = montecarlo.getPi();
	cout << "Pi = " << pi << endl;

	return MathTools::isEquals(pi, PI_FLOAT, 0.01f);
}

