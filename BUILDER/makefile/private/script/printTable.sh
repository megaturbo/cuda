#!/bin/bash
#


# ---------------------------------------
#                input
#----------------------------------------

# "$@" : container

# ---------------------------------------
#               private
#----------------------------------------

i=0
for element in $@
	do
		if [ $((i % 2)) -eq 0 ]; then
			echo -n "[CBI] ->              : " 
			echo -n ${element}
		else
			echo -n "                      "  
			echo -n -e '\t\t'
			echo ${element}
		fi
		
		
	i=$((i+1))
	done

# ---------------------------------------
#               end
#----------------------------------------
